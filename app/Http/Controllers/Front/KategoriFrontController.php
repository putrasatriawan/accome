    public function show_kategori($slug)
    {
        $kategori = Kategori::where('slug', $slug)->first();
        if ($kategori) {
            $talent = Talent::where('kategori_id', $kategori->id)->get();
            return view('front.view_slug.kategori_view_front')->with([
                'talent' => $talent,
                'kategori' => $kategori,
            ]);
        } else {
            return redirect('/');
        }
    }
